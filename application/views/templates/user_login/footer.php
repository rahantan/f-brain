<script>
    function password_show_hide(inputId) {
        var x = document.getElementById(inputId);
        var show_eye = document.getElementById("show_eye_" + inputId);
        var hide_eye = document.getElementById("hide_eye_" + inputId);

        hide_eye.classList.toggle("d-none");

        if (x.type === "password") {
            x.type = "text";
            show_eye.style.display = "none";
            hide_eye.style.display = "block";
        } else {
            x.type = "password";
            show_eye.style.display = "block";
            hide_eye.style.display = "none";
        }
    }
</script>

